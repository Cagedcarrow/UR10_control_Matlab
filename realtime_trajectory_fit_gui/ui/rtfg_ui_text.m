function textOut = rtfg_ui_text(action, data)
switch action
    case 'trajectorySummary'
        textOut = sprintf( ...
            'left=%.3f m | hMud=%.3f m | L=%.3f m | theta=%.1f deg | d=%.3f m | xPlane=%.3f m', ...
            data.leftWallOffset, data.mudHeight, data.approachLen, ...
            data.thetaDeg, data.depth, data.xPlane);
    case 'poseSummary'
        textOut = sprintf( ...
            'xyz=[%.3f, %.3f, %.3f] m | rpy=[%.1f, %.1f, %.1f] deg', ...
            data.x, data.y, data.z, ...
            data.rollDeg, data.pitchDeg, data.yawDeg);
    otherwise
        error('rtfg_ui_text:UnknownAction', 'Unknown action: %s', action);
end
end
