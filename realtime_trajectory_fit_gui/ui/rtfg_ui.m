function state = updateSummaries(state)
state.ui.lblTrajSummary.Text = rtfg_ui_text('trajectorySummary', state.trajParams);
state.ui.lblPoseSummary.Text = rtfg_ui_text('poseSummary', state.pose);
state = syncTrajectoryControls(state);
state = syncPoseControls(state);
end

function state = setStatus(state, text)
state.ui.lblStatus.Text = text;
drawnow limitrate nocallbacks;
end

function state = setProgress(state, fraction, text)
if isfield(state.ui, 'gaugeProgress') && isgraphics(state.ui.gaugeProgress)
    fraction = min(max(fraction, 0), 1);
    state.ui.gaugeProgress.Value = fraction;
    if isfield(state.ui, 'lblProgressValue') && isgraphics(state.ui.lblProgressValue)
        state.ui.lblProgressValue.Text = sprintf('%.0f%%', 100 * fraction);
    end
end
if nargin >= 3 && isfield(state.ui, 'gaugeProgress') && isgraphics(state.ui.gaugeProgress)
    state.ui.gaugeProgress.Tooltip = text;
end
drawnow limitrate nocallbacks;
end

function state = setRunEnabled(state, enabled)
if isfield(state.ui, 'btnRunPreview') && isgraphics(state.ui.btnRunPreview)
    state.ui.btnRunPreview.Enable = ternaryEnabled(enabled);
end
end

function out = ternaryEnabled(flag)
if flag
    out = 'on';
else
    out = 'off';
end
end

function [slider, valueLabel] = addSlider(parent, labelText, limits, value, changingFcn, changedFcn)
uilabel(parent, 'Text', labelText, 'FontSize', 11);
slider = uislider(parent, ...
    'Limits', limits, ...
    'Value', value, ...
    'MajorTicks', [], ...
    'MinorTicks', [], ...
    'ValueChangingFcn', @(~, evt) changingFcn(evt.Value), ...
    'ValueChangedFcn', @(src, ~) changedFcn(src.Value));
valueLabel = uilabel(parent, 'Text', '', 'FontSize', 10);
end

function closeChildFigure(fig, childClosedFcn, fieldName)
delete(fig);
childClosedFcn(fieldName);
end

function state = syncTrajectoryControls(state)
if isfield(state.ui, 'valWallOffset') && isgraphics(state.ui.valWallOffset)
    state.ui.sWallOffset.Value = state.trajParams.leftWallOffset;
    state.ui.valWallOffset.Text = sprintf('当前值: %.3f m', state.trajParams.leftWallOffset);
    state.ui.sMudHeight.Value = state.trajParams.mudHeight;
    state.ui.valMudHeight.Text = sprintf('当前值: %.0f mm', 1000 * state.trajParams.mudHeight);
    state.ui.sApproachLen.Value = state.trajParams.approachLen;
    state.ui.valApproachLen.Text = sprintf('当前值: %.3f m', state.trajParams.approachLen);
    state.ui.sTheta.Value = state.trajParams.thetaDeg;
    state.ui.valTheta.Text = sprintf('当前值: %.1f deg', state.trajParams.thetaDeg);
    state.ui.sDepth.Value = state.trajParams.depth;
    state.ui.valDepth.Text = sprintf('当前值: %.3f m', state.trajParams.depth);
    state.ui.sXPlane.Value = state.trajParams.xPlane;
    state.ui.valXPlane.Text = sprintf('当前值: %.3f m', state.trajParams.xPlane);
end
end

