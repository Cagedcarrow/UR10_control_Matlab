function varargout = rtfg_ui(action, varargin)
%RTFG_UI UI builders and summary/status synchronization.

switch action
    case 'buildMainUi'
        varargout{1} = buildMainUi(varargin{1}, varargin{2});
    case 'openTrajectoryWindow'
        varargout{1} = openTrajectoryWindow(varargin{1}, varargin{2});
    case 'openEnvironmentWindow'
        varargout{1} = openEnvironmentWindow(varargin{1}, varargin{2});
    case 'updateSummaries'
        varargout{1} = updateSummaries(varargin{1});
    case 'setStatus'
        varargout{1} = setStatus(varargin{1}, varargin{2});
    case 'setProgress'
        varargout{1} = setProgress(varargin{1}, varargin{2}, varargin{3});
    case 'setRunEnabled'
        varargout{1} = setRunEnabled(varargin{1}, varargin{2});
    otherwise
        error('rtfg_ui:UnknownAction', 'Unknown action: %s', action);
end
end

