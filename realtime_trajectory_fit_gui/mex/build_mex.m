function build_mex()
%BUILD_MEX Build the realtime trajectory fit GUI C++/MEX solver (modular).

thisDir = fileparts(mfilename('fullpath'));
srcDir = fullfile(thisDir, 'src');
binDir = fullfile(thisDir, 'bin');
if ~exist(binDir, 'dir')
    mkdir(binDir);
end

% Verify source files exist
srcFiles = {
    fullfile(srcDir, 'utils.cpp')
    fullfile(srcDir, 'robot_model.cpp')
    fullfile(srcDir, 'ik_solver.cpp')
    fullfile(srcDir, 'collision_checker.cpp')
    fullfile(srcDir, 'trajectory_solver.cpp')
    fullfile(srcDir, 'mex_entry.cpp')
    };
for i = 1:numel(srcFiles)
    if ~isfile(srcFiles{i})
        error('build_mex:MissingSource', 'Missing source file: %s', srcFiles{i});
    end
end

includeDirs = { ...
    '/usr/include/eigen3', ...
    '/usr/include', ...
    '/opt/ros/humble/include', ...
    srcDir};  % include our own headers

libDirs = { ...
    '/usr/lib/x86_64-linux-gnu', ...
    '/opt/ros/humble/lib', ...
    '/opt/ros/humble/lib/x86_64-linux-gnu'};

libs = { ...
    '-lorocos-kdl', ...
    '-lkdl_parser', ...
    '-lurdf', ...
    '-lurdf_xml_parser', ...
    '-lurdfdom_model', ...
    '-lurdfdom_model_state', ...
    '-lurdfdom_sensor', ...
    '-lurdfdom_world', ...
    '-lconsole_bridge', ...
    '-lfcl', ...
    '-lccd', ...
    '-loctomap', ...
    '-loctomath'};

args = {'-v', '-largeArrayDims', 'CXXFLAGS=$CXXFLAGS -std=c++17 -O3'};
for i = 1:numel(includeDirs)
    args{end + 1} = ['-I' includeDirs{i}]; %#ok<AGROW>
end
for i = 1:numel(libDirs)
    args{end + 1} = ['-L' libDirs{i}]; %#ok<AGROW>
end
args = [args, libs, srcFiles, {'-outdir', binDir}];

cwd = pwd;
cleanupObj = onCleanup(@() cd(cwd));
cd(thisDir);
mex(args{:});
end
